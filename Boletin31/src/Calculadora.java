    public void escribir(String numero){
        
        String texto = jTextField.getText();
        
        jTextField.setText(texto + numero);
        
    }
    
    public void operar(String operador){
        
        operando1 = jTextField.getText();
        
        signo = operador;
        
        jTextField.setText(" ");
        
    }
    
    public void calcular(){
    
        Double resultado = null;
                
        operando2 = jTextField.getText();
        
        Double op1 = Double.parseDouble(operando1);
        Double op2 = Double.parseDouble(operando2);
        
        switch (signo) {
            case "+":
                resultado = op1 + op2;
                break;
            case "-":
                resultado = op1 - op2;
                break;
            case "/":
                resultado = op1 / op2;
                break;
            case "*":
                resultado = op1 * op2;
                break;
        }
    
        jTextField.setText(String.valueOf(resultado));
        
    }
    
